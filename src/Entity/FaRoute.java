package Entity;

public class FaRoute {
    private String idUser;
    private String idRoute;

    public FaRoute(String idUser, String idRoute) {
        this.idUser = idUser;
        this.idRoute = idRoute;
    }

    public String getIdUser() {
        return idUser;
    }

    public String getIdRoute() {
        return idRoute;
    }
}
